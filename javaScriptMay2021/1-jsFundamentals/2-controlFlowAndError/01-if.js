/*  
IF CONDITIONALS
Conditionals are good at allowing our code to dynamically perform and run based upon different inputs given to it.
*/

let isOn = true;
if (isOn == true){
    console.log('The light is on!');
}

if (isOn){
    console.log('The light is on, yay!');
}

let weather = 75;

if (weather < 70){
    console.log('Wear a jacket!');
}

let string = 'Mello';

if (string === 'Mello' || false){
    console.log('if statements and conditions are powerful!');
}

